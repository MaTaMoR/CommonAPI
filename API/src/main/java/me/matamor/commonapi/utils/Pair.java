package me.matamor.commonapi.utils;

public class Pair<K, V> { //A simply object pair, like a map Entry

    private final K key;
    private final V value;

    public Pair(K key, V value) {
        this.key = key;
        this.value = value;
    }

    public K getKey() {
        return key;
    }

    public V getValue() {
        return value;
    }
}
